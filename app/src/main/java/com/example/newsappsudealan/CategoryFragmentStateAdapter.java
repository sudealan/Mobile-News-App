package com.example.newsappsudealan;

import androidx.annotation.NonNull;
import androidx.fragment.app.Fragment;
import androidx.fragment.app.FragmentActivity;
import androidx.viewpager2.adapter.FragmentStateAdapter;

import java.util.List;

class CategoryFragmentStateAdapter extends FragmentStateAdapter {

    private List<CategoryFragment> fragments;

    public CategoryFragmentStateAdapter(@NonNull FragmentActivity fragmentActivity, List<CategoryFragment> fragments) {
        super(fragmentActivity);
        this.fragments = fragments;
    }

    @NonNull
    @Override
    public Fragment createFragment(int position) {
        return fragments.get(position);
    }

    @Override
    public int getItemCount() {
        return fragments.size();
    }
}
